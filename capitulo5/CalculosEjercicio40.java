package capitulo5;

public class CalculosEjercicio40 {
    
    public static double calcularRaiz(double numero){
        double raiz;
        raiz = Math.sqrt(numero);
        return raiz;
    }
    public static double calcularCuadrado(double numero){
        double cuadrado;
        cuadrado = Math.pow(numero, 2);
        return cuadrado;
    }
    
    public static double calcularCubo(double numero){
        double cubo;
        cubo = Math.pow(numero, 3);
        return cubo;
    }
}
